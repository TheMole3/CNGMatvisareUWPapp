namespace CNG_Matvisare_v2
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        private async void Button_Click(object sender, RoutedEventArgs e)
        {
            AppListEntry entry = (await Package.Current.GetAppListEntriesAsync())[0];
            await StartScreenManager.GetDefault().RequestAddAppListEntryAsync(entry);

            ContentDialog done = new ContentDialog()
            {
                Title = "CNG Matvisare är installerad",
                Content = "Du kan nu trycka på Windows Knappen och se maten",
                CloseButtonText = "Okej"
            };

            await done.ShowAsync();
        }
    }
}
